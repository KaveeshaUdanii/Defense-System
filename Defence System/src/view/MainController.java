    public void updateOtherFramesStatus(boolean areaClear) {
        String status = areaClear ? "Area cleared" : "Area not clear";
        hellicopter.updateStatusLabel(status);
        submarine.updateStatusLabel(status);
        tank.updateStatusLabel(status);
    }
    public void updateTextArea(String text, String unitName) {
        jTextArea1.append(unitName + ": " + text + "\n");
    }
